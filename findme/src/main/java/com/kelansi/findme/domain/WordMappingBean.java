package com.kelansi.findme.domain;

public class WordMappingBean {
	private String field;
	
	private String mappingStr;
	
	private Integer enumNum;

	public String getField() {
		return field;
	}

	public void setField(String field) {
		this.field = field;
	}

	public String getMappingStr() {
		return mappingStr;
	}

	public void setMappingStr(String mappingStr) {
		this.mappingStr = mappingStr;
	}

	public Integer getEnumNum() {
		return enumNum;
	}

	public void setEnumNum(Integer enumNum) {
		this.enumNum = enumNum;
	}
}
